    private void loadFacusties() {
        facultycombo.removeAllItems();
        facultyModels.clear();
        facultyModels = facultyController.getFaculties();
        facultyModels.forEach((data) -> facultycombo.addItem(data.getFaculty_name()));
    }

    private void loadCenters() {
        centercombo.removeAllItems();
        centerModels.clear();
        centerModels = centerController.getFaculties();
        centerModels.forEach((data) -> centercombo.addItem(data.getCenter_name()));
    }

    private void loadLevels() {
        level.removeAllItems();
        levelModels.clear();
        levelModels = levelsController.getFaculties();
        levelModels.forEach((data) -> level.addItem(data.getCenter_name()));
    }

    private void clearAll() {
        name.setText("");
        empid.setText("");
        dept.setText("");
        building.setSelectedIndex(0);
        level.setSelectedIndex(0);
        centercombo.setSelectedIndex(0);
        facultycombo.setSelectedIndex(0);
    }

    private void getModels() {
        defaultTableModel.setRowCount(0);
        lecturerModels.clear();
        lecturerModels = lecturerController.getAll();
        if (lecturerModels.size() > 0) {
            lecturerModels.forEach((l) -> {
                                
                Vector vectorData = new Vector();
                vectorData.add(l.getName());
                vectorData.add(l.getLevels().getCenter_id() + "." + l.getEmployee_id());
                vectorData.add(l.getDepartmemnt());
                vectorData.add(l.getFaculty().getFaculty_name());
                vectorData.add(l.getCenter().getCenter_name());
                vectorData.add(l.getBuilding().getBuilding_name());
                vectorData.add(l.getLevels().getCenter_name());

                defaultTableModel.addRow(vectorData);
            });
        }

    }

    private void loadLocation() {
        building.removeAllItems();
        locationModels.clear();
        locationModels = locationController.getLocations();
        locationModels.forEach((data) -> {
            building.addItem(data.getBuilding_name());
        });
    }
