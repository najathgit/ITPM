    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (empid.getText().length() ==6) {
            if (!name.getText().isEmpty() && !empid.getText().isEmpty() && !dept.getText().isEmpty() && building.getItemCount() > 0) {
                int x = lecturerController.saveLecture(new Lecturer(empid.getText(), name.getText(), dept.getText(), new LocationModel(locationModels.get(building.getSelectedIndex()).getLocation_id()), new Faculty(facultyModels.get(facultycombo.getSelectedIndex()).getFaculty_id()), new Center(centerModels.get(centercombo.getSelectedIndex()).getCenter_id()), new Levels(levelModels.get(centercombo.getSelectedIndex()).getCenter_id()), 1));
                if (x == 1) {
                    clearAll();
                    JOptionPane.showMessageDialog(null, "Saved");
                    getModels();
                } else {
                    JOptionPane.showMessageDialog(null, "Something Wrong");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please fill all the data before save the record");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Employee number needs to place with least 6 characters");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

