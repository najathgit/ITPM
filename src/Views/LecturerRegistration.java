    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {

            Lecturer lecturer = lecturerModels.get(jTable1.getSelectedRow());

            name.setText(lecturer.getName());
            empid.setText(lecturer.getEmployee_id());
            dept.setText(lecturer.getDepartmemnt());

            int levelDataIndex = 0;
            int cenDataIndex = 0;
            int facDataIndex = 0;
            int locationDataIndex = 0;

            for (Levels level1 : levelModels) {
                if (level1.getCenter_id() == lecturer.getLevels().getCenter_id()) {
                    level.setSelectedIndex(levelDataIndex);
                    break;
                }
                levelDataIndex++;
            }

            for (Center center1 : centerModels) {
                if (center1.getCenter_id() == lecturer.getCenter().getCenter_id()) {
                    centercombo.setSelectedIndex(cenDataIndex);
                    break;
                }
                cenDataIndex++;
            }

            for (Faculty fac1 : facultyModels) {
                if (fac1.getFaculty_id() == lecturer.getFaculty().getFaculty_id()) {
                    facultycombo.setSelectedIndex(facDataIndex);
                    break;
                }
                facDataIndex++;
            }

            for (LocationModel locationObj : locationModels) {
                if (locationObj.getLocation_id() == lecturer.getBuilding().getLocation_id()) {
                    building.setSelectedIndex(locationDataIndex);
                    break;
                }
                locationDataIndex++;
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Nothing Selected");
        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (empid.getText().length()==6) {
            if (!name.getText().isEmpty() && !empid.getText().isEmpty() && !dept.getText().isEmpty() && building.getItemCount() > 0) {
                int x = lecturerController.updateLecture(new Lecturer(empid.getText(), name.getText(), dept.getText(), new LocationModel(locationModels.get(building.getSelectedIndex()).getLocation_id()), new Faculty(facultyModels.get(facultycombo.getSelectedIndex()).getFaculty_id()), new Center(centerModels.get(centercombo.getSelectedIndex()).getCenter_id()), new Levels(levelModels.get(level.getSelectedIndex()).getCenter_id()), 1));
                if (x == 1) {
                    clearAll();
                    JOptionPane.showMessageDialog(null, "Updated");
                    getModels();
                } else {
                    JOptionPane.showMessageDialog(null, "Something Wrong");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please fill all the data before update the record");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Employee number needs to place with least 6 characters");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

