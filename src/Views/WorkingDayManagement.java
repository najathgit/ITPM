    private void loadDays() {
        dayModelList.clear();
        dayModelList = workingDayManagementController.loadDays();
        dayList.removeAllItems();
        dayModelList.forEach((data) -> dayList.addItem(data.getDay_name()));
    }

    private void loadWorkingRecords() {
        workingDayManagementModelList.clear();
        workingDayManagementModelList = workingDayManagementController.loadWorkingDayManagement();
        defaultTableModel.setRowCount(0);
        workingDayManagementModelList.forEach((data) -> {
            Vector vectorData = new Vector();
            vectorData.add(data.getWorking_days());
            vectorData.add(data.getWorking_time_per_day());
            vectorData.add(data.getStarting_time());
            vectorData.add(data.getEnding_time());

            defaultTableModel.addRow(vectorData);
        });
    }

    private void clearAll() {
        newDaysList.clear();
        woringtimeperday.setText("");
        noofweddingdays.setText("");
        dayList.setSelectedIndex(0);
        statingtime_hh.setSelectedIndex(0);
        statingtime_mm.setSelectedIndex(0);
        statingtime_ss.setSelectedIndex(0);
        endingtime_hh.setSelectedIndex(0);
        endingtime_mm.setSelectedIndex(0);
        endingtime_ss.setSelectedIndex(0);
    }

    private void loadNewDayList() {
        workingdayscombo.removeAllItems();
        newDaysList.forEach((data) -> workingdayscombo.addItem(data.getDay_name()));
    }
