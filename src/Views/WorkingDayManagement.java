    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (!noofweddingdays.getText().isEmpty()) {
                if (Integer.parseInt(noofweddingdays.getText()) == newDaysList.size()) {
                    if (!noofweddingdays.getText().isEmpty() && !woringtimeperday.getText().isEmpty() && newDaysList.size() > 0) {
                        int x = workingDayManagementController.store(new WorkingDayManagementModel(woringtimeperday.getText(), statingtime_hh.getSelectedItem().toString() + ":" + statingtime_mm.getSelectedItem().toString() + " " + statingtime_ss.getSelectedItem().toString(), endingtime_hh.getSelectedItem().toString() + ":" + endingtime_mm.getSelectedItem().toString() + " " + endingtime_ss.getSelectedItem().toString(), 1, Integer.parseInt(noofweddingdays.getText())), newDaysList);
                        if (x == 1) {
                            JOptionPane.showMessageDialog(null, "Record Saved");
                            loadWorkingRecords();
                            loadNewDayList();
                            clearAll();
                        } else {
                            JOptionPane.showMessageDialog(null, "Something Wrong");
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Fields cannot be empty");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Day count not matching");
                }
            } else {
                JOptionPane.showMessageDialog(null, "No of working days empty");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please recheck the field values");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

