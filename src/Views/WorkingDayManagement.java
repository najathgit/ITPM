    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        check = true;
        DayModel dModel = dayModelList.get(dayList.getSelectedIndex());
        newDaysList.forEach((dayObj) -> {
            if (dModel.getDay_id() == dayObj.getDay_id()) {
                check = false;
            }
        });

        if (check) {
            newDaysList.add(dayModelList.get(dayList.getSelectedIndex()));
        }

        loadNewDayList();
    }//GEN-LAST:event_jButton6ActionPerformed

    private boolean removeCheck = false;

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        try {
            DayModel dModel = newDaysList.get(workingdayscombo.getSelectedIndex());
            int x = 0;
            removeCheck = false;
            for (DayModel dd : newDaysList) {
                if (dModel.getDay_id() == dd.getDay_id()) {
                    removeCheck = true;
                    break;
                }
                x++;
            }

            if (removeCheck) {
                newDaysList.remove(x);
            }

            loadNewDayList();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton7ActionPerformed

